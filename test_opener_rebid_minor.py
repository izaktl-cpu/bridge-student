_LOW = {
    'S': '2S 3S 4S 5S 6S 7S 8S 9S'.split(),
    'H': '2H 3H 4H 5H 6H 7H 8H 9H'.split(),
    'D': '2D 3D 4D 5D 6D 7D 8D 9D'.split(),
    'C': '2C 3C 4C 5C 6C 7C 8C 9C'.split(),
}

def hand(s=0, h=0, d=0, c=0, hcp=12):
    """
    בונה יד עם s/h/d/c קלפים בדיוק ו-hcp נקודות גבוהות בדיוק.
    מתחיל עם קלפים נמוכים (2-9) ומחליף ב-A,K,Q,J לפי הצורך.
    """
    counts = {'S': s, 'H': h, 'D': d, 'C': c}
    assert sum(counts.values()) == 13, f'סה״כ {sum(counts.values())} != 13'
    result = []
    for suit in 'SHDC':
        result.extend(_LOW[suit][:counts[suit]])
    remaining = hcp
    for suit in sorted('SHDC', key=lambda x: -counts[x]):
        if counts[suit] == 0:
            continue
        for rank, val in [('A', 4), ('K', 3), ('Q', 2), ('J', 1)]:
            if remaining <= 0:
                break
            if val > remaining:
                continue  # דלג על קלף יקר מדי, נסה קטן יותר
            for i, card in enumerate(result):
                if card.endswith(suit) and card[0].isdigit():
                    result[i] = f'{rank}{suit}'
                    remaining -= val
                    break
    return result

# ── מנגנון בדיקה ─────────────────────────────────────────────────────────────
passed = failed = 0
failures = []

def section(title):
    print(f'\n{"═"*56}\n  {title}\n{"═"*56}')

def chk(label, opening, response, n_hand, want):
    global passed, failed
    got, _ = opener_rebid(n_hand, opening, response)
    if got == want:
        passed += 1
    else:
        failed += 1
        msg = f'FAIL  {label}\n      got={got!r}  want={want!r}'
        failures.append(msg)
        print(msg)


